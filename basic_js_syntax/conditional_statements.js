// conditional_statemment 

// use prompt and alert , for taking input and printing something as popup messages

// switch-case

let day = prompt("what's the day?")

switch(day){
    case "monday":
        alert("monday")
    break; 
    case "tuesday":
        alert("tuesday")
    break;
    case "wednesday":
        alert("wednesday")
    break;
    case "thrusday":
        alert("thrusday")
    break;
    case "friday":
        alert("friday")
    break;
    case "saturday":
        alert("saturday")
    break;
    case "sunday":
        alert("sunday")
}
